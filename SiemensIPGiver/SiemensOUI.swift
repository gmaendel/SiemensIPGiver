        0xE87F95, 0xE8A245, 0xEC1F72, 0xF8F005, 0xFCB058, 0xFCDE79,
    ]

    static func isSiemens(_ mac: MACAddress) -> Bool {
        guard mac.bytes.count >= 3 else { return false }
        let oui = (UInt32(mac.bytes[0]) << 16)
            | (UInt32(mac.bytes[1]) << 8)
            | UInt32(mac.bytes[2])
        return prefixes.contains(oui)
    }
}
