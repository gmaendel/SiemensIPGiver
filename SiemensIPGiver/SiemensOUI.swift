import Foundation

nonisolated enum SiemensOUI {
    static let prefixes: Set<UInt32> = [
        0x000123, 0x00098E, 0x000E8C, 0x000F69, 0x001160, 0x00120D,
        0x0015F2, 0x001B1B, 0x001B9D, 0x001C06, 0x001FF8, 0x002142,
        0x002342, 0x00238A, 0x002434, 0x002511, 0x002675, 0x0027EA,
        0x041BBA, 0x04489A, 0x04D3B5, 0x080006, 0x14EF1A, 0x208756,
        0x24EA40, 0x286336, 0x289C6E, 0x2C9924, 0x30055C, 0x302DAA,
        0x30FD21, 0x3431C4, 0x386C9B, 0x38EAA7, 0x3CBDD8, 0x40F413,
        0x44A842, 0x484BAA, 0x4C587C, 0x4CCC7A, 0x4CE705, 0x501AC5,
        0x501CB0, 0x543D37, 0x5C49AA, 0x5C7225, 0x5CA2EC, 0x60735C,
        0x644665, 0x64CB89, 0x64D015, 0x6C4665, 0x6C673D, 0x702E22,
        0x7072CF, 0x7492FB, 0x784476, 0x78A683, 0x7C5A1C, 0x80006A,
